from collections import deque

d = deque([10,20,30])
print(d)
d.append(40)
print(d)
d.appendleft(5)
print(d)
d.pop()
print(d)
d.popleft()
print(d)
d.extend([40,50,60])
print(d)
d.extendleft([5,4,3,2,1])
print(d)
d.rotate(1)
print(d)
d.rotate(3)
print(d)
d.rotate(-1)
print(d)

m = deque(maxlen=3)
m.append(1)
m.append(2)
m.append(3)
print(m)

m.append(4)
print(m)
